var rref = require('../');
var test = require('tape');

test(function (t) {
    t.plan(3);
    
    t.deepEqual(
        rref([
            [ 1, 2, -1, -4],
            [ 2, 3, -1,-11],
            [-2, 0, -3, 22]
        ]),
        [
            [ 1, 0, 0, -8 ],
            [ 0, 1, 0, 1 ],
            [ 0, 0, 1, -2 ]
        ]
    );
    t.deepEqual(
        rref([
            [ 2, 1, -1, 8 ],
            [ -3, -1, 2, -11 ],
            [ -2, 1, 2, -3 ]
        ]),
        [
            [ 1, 0, 0, 2 ],
            [ 0, 1, 0, 3 ],
            [ 0, 0, 1, -1 ]
        ]
    );
    t.deepEqual(
        rref([
            [ 100, 1, 1, 213],
            [ 300, 1, 1, 613],
            [ 100, 3, 1, 219],
            [ 200, 2, 1, 416],
            [ 300, 3, 1, 619]
        ]),
        [
            [ 1, 0, 0,  2 ],
            [ 0, 1, 0,  3 ],
            [ 0, 0, 1, 10 ],
            [ 0, 0, 0,  0 ],
            [ 0, 0, 0,  0 ]
        ]
    );
});
